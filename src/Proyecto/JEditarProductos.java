    private void btnEditarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarClienteActionPerformed
    String codigoEditar = txtcodigoP.getText().trim();
    String nuevoNombre = txtnombreP.getText().trim();
    String nuevoPrecio = txtprecioNuevo.getText().trim();
    String nuevoStock = txtstockNuevo.getText().trim();

    if (codigoEditar.isEmpty() || nuevoNombre.isEmpty() || nuevoPrecio.isEmpty() || nuevoStock.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Complete todos los campos.");
        return;
    }

    File archivo = new File("productos.txt");
    File archivoTemp = new File("productos_temp.txt");
    boolean encontrado = false;

    try (
        BufferedReader br = new BufferedReader(new FileReader(archivo));
        BufferedWriter bw = new BufferedWriter(new FileWriter(archivoTemp))
    ) {
        String linea;
        boolean primera = true;

        while ((linea = br.readLine()) != null) {
            if (primera) {
                bw.write(linea);
                bw.newLine();
                primera = false;
                continue;
            }

            String[] datos = linea.split(";");

            if (datos.length == 4 && datos[1].equalsIgnoreCase(codigoEditar)) {
                // Escribimos la línea con los nuevos datos
                String nuevaLinea = nuevoNombre + ";" + codigoEditar + ";" + nuevoPrecio + ";" + nuevoStock;
                bw.write(nuevaLinea);
                encontrado = true;
            } else {
                // Escribimos la línea original
                bw.write(linea);
            }
            bw.newLine();
        }

    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error al procesar el archivo.");
        return;
    }

    if (archivo.delete()) {
        archivoTemp.renameTo(archivo);
    }

    if (encontrado) {
        JOptionPane.showMessageDialog(this, "Producto editado exitosamente.");
        mostrarProductos(); // Recarga la tabla si la tienes
    } else {
        JOptionPane.showMessageDialog(this, "Producto no encontrado.");
    }
    }//GEN-LAST:event_btnEditarClienteActionPerformed

