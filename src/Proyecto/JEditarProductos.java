    private void mostrarProductos() {
    DefaultTableModel model = new DefaultTableModel();
    model.setColumnIdentifiers(new String[]{"Producto", "Código", "Precio", "Stock"});

    try (BufferedReader br = new BufferedReader(new FileReader("productos.txt"))) {
        String linea;
        boolean primera = true;

        while ((linea = br.readLine()) != null) {
            if (primera) {
                primera = false;
                continue;
            }

            String[] datos = linea.split(";");
            if (datos.length == 4) {
                model.addRow(datos);
            }
        }

        tbMostrarProducto.setModel(model); // Asegúrate de que "tabla" es tu JTable
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error al leer los productos.");
    }
}

